package EjerciciosWhile;

public class NumerosPares {

	public static void main(String[] args) {
		/* Crea un programa que muestre los números pares del 20 al 2, decreciendo, usando "while" */
		
		 int c=21;

         while (c>=3) {
        	 

                    c--;

                    System.out.println(+c);



         }

}

}                                           
